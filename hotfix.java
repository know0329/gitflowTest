import java.util.*;
public class hotfix {
    String bug;
    Integer numOfBug;
    public static void main(String args[]){
        hotfix gitflowfix = new hotfix();
        gitflowfix.bug = "gitBUG";
        gitflowfix.numOfBug = 50;

        System.out.println(gitflowfix.bug + gitflowfix.numOfBug);
    }
}
